package service

import (
	"context"

	"github.com/wso2-open-operations/grc-tools/apps/grc-platform/backend/internal/audit/model"
	"github.com/wso2-open-operations/grc-tools/apps/grc-platform/backend/internal/audit/repository"
)

// UserService exposes the user list for owner/auditor dropdowns in the UI.
// When Asgardeo SCIM2 is integrated, this service will proxy to Asgardeo instead.
type UserService interface {
	List(ctx context.Context) ([]*model.UserRef, error)
}

type userService struct {
	repo repository.UserRepository
}

func NewUserService(repo repository.UserRepository) UserService {
	return &userService{repo: repo}
}

func (s *userService) List(ctx context.Context) ([]*model.UserRef, error) {
	return s.repo.List(ctx)
}
